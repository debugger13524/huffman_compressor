class Pair {
  String _character, _code;

  Pair() {
    _character = _code = "";
  }

  void setCharacter(String character) {
    _character = character;
  }

  void setCode(String code) {
    _code = code;
  }

  String getCharacter() {
    return _character;
  }

  String getCode() {
    return _code;
  }
}